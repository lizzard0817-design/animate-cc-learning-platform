export function getQuestionsByLevel(levelId: number): Question[] {
  return DEFAULT_QUESTIONS.filter(q => q.level_id === levelId)
}

export function getQuestionById(id: string): Question | undefined {
  return DEFAULT_QUESTIONS.find(q => q.id === id)
}

export function getStats(): { total: number; byLevel: Record<number, number> } {
  const byLevel: Record<number, number> = {}
  DEFAULT_QUESTIONS.forEach(q => {
    byLevel[q.level_id] = (byLevel[q.level_id] || 0) + 1
  })
  return { total: DEFAULT_QUESTIONS.length, byLevel }
}
